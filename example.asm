.DSEG
.ORG 0x0100

.CSEG
.ORG 0x0000
LDI R17, 0b11111111
OUT DDRC,R17
JMP LOOP

LOOP:
  LDI R16, 0b00001000
  OUT PORTC,R16
  JMP WAIT
  LDI R16, 0b00000100
  OUT PORTC,R16
  JMP WAIT
  LDI R16, 0b00000010
  OUT PORTC,R16
  JMP WAIT
  LDI R16, 0b00000001
  OUT PORTC,R16
  JMP WAIT
  JMP LOOP

WAIT:
  LDI R0,0xFF
WAIT1:
  LDI R1,0xFF
WAIT2:
  DEC R1
  BRNE WAIT2
  DEC R0
  BRNE WAIT1
  RET
